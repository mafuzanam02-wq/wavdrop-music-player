package com.launchpoint.wavdrop.data.settings

/**
 * In-app accent color theme. Display names intentionally mirror [AppIconChoice] so the
 * user can keep their icon and accent color visually consistent.
 */
enum class AccentColor(val displayName: String) {
    MIDNIGHT_VIOLET("Midnight Violet"),
    CLEAN_PURPLE("Clean Purple"),
    DEEP_TEAL("Deep Teal"),
}
